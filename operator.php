<?php

$a = 5;
$b = 10;

# operator aritmatika standar
echo "Aritamtika <hr>";
echo "{$a} + {$b} = " . ($a + $b) . " <br>";
echo "{$a} - {$b} = " . ($a - $b) . " <br>";
echo "{$a} * {$b} = " . ($a * $b) . " <br>";
echo "{$a} / {$b} = " . ($a / $b) . " <br>";
echo "<br>";

# modulus
echo "Modulus <hr>";
echo "{$a} % {$b} = " . ($a % $b) . " <br>";
echo "{$b} % {$a} = " . ($b % $a) . " <br>";
echo "<br>";

# eksponensial
echo "<h1>Eksponensial</h1> <hr>";
echo "{$a} ** {$b} = " . ($a ** $b) . " <br>";
echo "{$b} ** {$a} = " . ($b ** $a) . " <br>";
echo "<br>";

# negasi
echo "Negasi <hr>";
echo "-a = " . (-$a) . " <br>";
echo "-b = " . (-$b) . " <br>";
echo "<h1>Penugasan</h1>";
echo "<hr>";